// The executed-token set hides in a closure behind a Proxy over `document.evaluate`:
// patching the existing slot in place adds no page-enumerable property.
function guardRunOnce(secret, token, orig, args, thisArg, logError) {
  function run() {
    try {
      orig.apply(thisArg, args);
      return true;
    } catch (e) {
      if (logError) logError(e);
      return undefined;
    }
  }

  if (!secret || !token) return run();

  // Native `evaluate` throws on these arguments; only the guard proxy answers, echoing the secret.
  function ask(claim) {
    try {
      var answer = document.evaluate(secret, token, claim);
      return answer && typeof answer === 'object' && answer.s === secret ? answer : null;
    } catch {
      return null;
    }
  }

  var status = ask(false);

  if (!status) {
    try {
      var evaluate = document.evaluate;
      if (typeof evaluate !== 'function') return run();

      var owner = document;
      while (owner && !Object.prototype.hasOwnProperty.call(owner, 'evaluate')) {
        owner = Object.getPrototypeOf(owner);
      }
      if (!owner) return run();

      // Keyed by document: in Firefox the patched prototype survives the initial about:blank swap.
      var store = new WeakMap();
      Object.defineProperty(owner, 'evaluate', {
        value: new Proxy(evaluate, {
          apply: function (target, receiver, callArgs) {
            if (callArgs[0] !== secret || !receiver)
              return Reflect.apply(target, receiver, callArgs);
            var tokens = store.get(receiver);
            if (!tokens) {
              tokens = new Set();
              store.set(receiver, tokens);
            }
            if (callArgs[2]) {
              tokens.add(callArgs[1]);
              return { s: secret };
            }
            return { s: secret, has: tokens.has(callArgs[1]) };
          },
        }),
        writable: true,
        enumerable: false,
        configurable: true,
      });
    } catch {
      return run();
    }

    status = ask(false);
    if (!status) return run();
  }

  if (status.has) return false;

  // Claim before running so a scriptlet that throws mid-way is not re-run against a mutated document.
  ask(true);
  return run();
}

export function wrapScriptletSource(originalSource, name = 'scriptlet') {
  const logError = `function (e) { console.error(${JSON.stringify(`[adblocker] ${name} failed:`)}, e); }`;

  return `function (__secret, __token, ...__args) {
  return (${guardRunOnce.toString()})(__secret, __token, (${originalSource}), __args, this, ${logError});
}`;
}

const BANNER = `/* eslint-disable */
// Generated by scripts/generate-scriptlets.js — DO NOT EDIT.
// Each scriptlet func is wrapped with an in-document idempotency guard.`;

function emitEntry(name, entry) {
  if (typeof entry.func !== 'function') {
    throw new Error(
      `[generate-scriptlets] expected "func" to be a function for scriptlet "${name}", got ${typeof entry.func}`,
    );
  }

  const key = JSON.stringify(name);
  const world = entry.world ? `\n  world: ${JSON.stringify(entry.world)},` : '';
  const aliases = (entry.aliases || [])
    .map((alias) => `\nSCRIPTLETS[${JSON.stringify(alias)}] = SCRIPTLETS[${key}];`)
    .join('');

  return `SCRIPTLETS[${key}] = {${world}
  func: ${wrapScriptletSource(entry.func.toString(), name)},
};${aliases}`;
}

export function generateScriptletsModule(source) {
  // The package repeats each entry under its aliases, canonical name first; dedupe by identity.
  const seen = new Set();
  const entries = Object.entries(source).filter(([, entry]) => !seen.has(entry) && seen.add(entry));

  const blocks = entries.map(([name, entry]) => emitEntry(name, entry));

  return `${BANNER}
const SCRIPTLETS = {};

${blocks.join('\n\n')}

export default SCRIPTLETS;
`;
}
