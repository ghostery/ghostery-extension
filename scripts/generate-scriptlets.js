import { writeFileSync } from 'node:fs';
import { fileURLToPath, pathToFileURL } from 'node:url';

import scriptlets from '@ghostery/scriptlets';

import { wrapScriptletSource } from './utils/wrap-scriptlet.js';

const OUTPUT_PATH = fileURLToPath(
  new URL('../src/background/adblocker/scriptlets.generated.js', import.meta.url),
);

// Debug-build-only counting scriptlet: each run bumps `self[args[0]]`, letting
// the e2e spec assert exactly-once execution however many triggers fired.
export const TEST_SCRIPTLET_NAME = '__e2e-inc.js';

const TEST_SCRIPTLET_SOURCE = `function (scriptletGlobals = {}, ...args) {
  self[args[0]] = (self[args[0]] || 0) + 1;
}`;

const BANNER = `/* eslint-disable */
// Generated by scripts/generate-scriptlets.js — DO NOT EDIT.
// Each scriptlet func is wrapped with an in-document idempotency guard.`;

function emitEntry(name, entry, { debug }) {
  if (typeof entry.func !== 'function') {
    throw new Error(
      `[generate-scriptlets] expected "func" to be a function for scriptlet "${name}", got ${typeof entry.func}`,
    );
  }

  const lines = [`  aliases: ${JSON.stringify(entry.aliases || [])},`];
  if (entry.world) lines.push(`  world: ${JSON.stringify(entry.world)},`);
  lines.push(`  requiresTrust: ${entry.requiresTrust ? 'true' : 'false'},`);
  lines.push(`  func: ${wrapScriptletSource(entry.func.toString(), { debug, name })},`);

  return `SCRIPTLETS[${JSON.stringify(name)}] = {\n${lines.join('\n')}\n};`;
}

export function generateScriptletsModule(
  source,
  { includeTestScriptlets = false, debug = false } = {},
) {
  // The package expands aliases as extra keys on the same object, canonical name
  // first; dedupe by identity to recover the canonical entries in order.
  const seen = new Set();
  const blocks = [];

  for (const [name, entry] of Object.entries(source)) {
    if (seen.has(entry)) continue;
    seen.add(entry);
    blocks.push(emitEntry(name, entry, { debug }));
  }

  if (includeTestScriptlets) {
    blocks.push(
      `SCRIPTLETS[${JSON.stringify(TEST_SCRIPTLET_NAME)}] = {\n` +
        `  aliases: [],\n` +
        `  requiresTrust: false,\n` +
        `  func: ${wrapScriptletSource(TEST_SCRIPTLET_SOURCE, { debug, name: TEST_SCRIPTLET_NAME })},\n` +
        `};`,
    );
  }

  return `${BANNER}
const SCRIPTLETS = {};

${blocks.join('\n\n')}

const scriptlets = {};
for (const [name, scriptlet] of Object.entries(SCRIPTLETS)) {
  scriptlets[name] = scriptlet;
  for (const alias of scriptlet.aliases) {
    scriptlets[alias] = scriptlet;
  }
}

export default scriptlets;
`;
}

function main() {
  const debug = process.argv.includes('--debug');
  const module = generateScriptletsModule(scriptlets, { includeTestScriptlets: debug, debug });

  writeFileSync(OUTPUT_PATH, module);

  console.log(
    `Generated scriptlets.generated.js (${Object.keys(scriptlets).length} scriptlets${
      debug ? ' + e2e test scriptlet' : ''
    })`,
  );
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  main();
}
